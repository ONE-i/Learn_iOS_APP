import UIKit

// Addition
var a = 20 + 5

// Subtraction
var b = 20 - 5

// Multiplication
var c = 20 * 5

// Division
var d = 20 / 5

// Modulus
var e = 20 % 2

// Equation with variables
var f = (a * b) + (c / d)

// Increment the variable
f = f + 1

// or...
f += 1

// Decrement the variable
f -= 1

// Multiply the variable
f *= 2

// Divide the variable
f /= 4


// Additional operators

// Absolute number
var g = abs(-1)

// Ceiling
var h = ceil(1.9)

// Floor
var i = floor(1.4)

// Power
var k = pow(2, 4)
